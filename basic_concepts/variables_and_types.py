# Variables and Data Types
# 1. String Variable
name = "Python"
print(name)

# 2. Integer Variable
age = 3
print(age)

# 3. Float Variable
height = 1.75
print(height)

# 4. Boolean Variable
is_programming_language = True
print(is_programming_language)

# 5. Multiple Variable Assignment
x, y, z = 1, 2, 3
print(x, y, z)

# 6. Type Conversion
num_str = "100"
num_int = int(num_str)
print(num_int)

# 7. List Variable
fruits = ["apple", "banana", "cherry"]
print(fruits)

# 8. Dictionary Variable
person = {"name": "Alice", "age": 25}
print(person)

# 9. Tuple Variable
coordinates = (10.0, 20.0)
print(coordinates)

# 10. Set Variable
unique_numbers = {1, 2, 3, 4, 5}
print(unique_numbers)

# 11. Dynamic Typing
var = "Hello"
print(var)
var = 10
print(var)

# 12. Length of a String
greeting = "Hello, World!"
print(len(greeting))

# 13. Accessing Characters in a String
first_char = greeting[0]
print(first_char)

# 14. String Slicing
substring = greeting[0:5]
print(substring)

# 15. Escape Characters
escaped_string = "She said, \"Hello!\""
print(escaped_string)

# 16. String Concatenation
full_greeting = greeting + " How are you?"
print(full_greeting)

# 17. String Formatting (f-strings)
name = "Alice"
age = 25
print(f"{name} is {age} years old.")

# 18. Checking Data Types
print(type(name))
print(type(age))

# 19. Constants (Convention)
PI = 3.14
print(PI)

# 20. Using None
value = None
print(value)
